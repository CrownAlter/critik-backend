package com.application.critik.entities;

/**
 * Enum defining the types of reactions users can make to artworks.
 * 
 * Available reaction types:
 * - AGREE: User agrees with the artwork interpretation
 * - DISAGREE: User disagrees with the artwork interpretation
 * 
 * Future expansion could include: INSIGHTFUL, THOUGHT_PROVOKING, etc.
 */
public enum ReactionType {
    AGREE, 
    DISAGREE
}
